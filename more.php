	<h1 align="center">Smt. Anandiben Patel</h1>
	<h1 align="center">Governer of Utter Pradesh</h1>
	<br/>
	<p>Member of Rajya Sabha, 1994-1998.
In 1998, was elected from Mandal assembly constituency Ahmedabad, and became MLA. From 1998 to 2002, was the Minister of Education (primary, secondary, and adult) and Minister of Women & Child Welfare.</p>
<br/>
  <p>In 2002, became the legislator from Patan Vidhan Sabha constituency for the second time and proved the belief wrong that the Gujarat Education Minister can not win consequetive elections.Was the Minister of Education (primary, secondary, and adult) and Minister of, Higher and Technical Education, Women and Child Welfare, Sports, Youth and Cultural Activity from the year 2002 till 2007.</p>
  <p>In the year 2007, became the legislator from Patan Vidhan Sabha constituency for the third time. From 2007 to 2012, she was the Minister of Revenue, Disaster Management, Road and Building, Capital Project, Women and Child Welfare.</p>
<br/>
  <p>In 2012, was elected for the fourth consecutive time MLA from the Ghatlodia assembly constituency in the Ahmedabad city and won the most votes in the state. She was the Minister of Revenue, Drought Relief, Land Reform, Rehabilitation, Reconstruction, Road and Building, Capital Project, Urban Development and Urban Housing from 2012 to 2014.</p>
<br/>
  <p>From 22nd May 2014 to 7th August, 2016, was the first woman Chief Minister of the Gujarat State.
  Was the Governor of Chhattisgarh from 15 August 2018 till 28 July 2019.</p>
<br/>
  <p>Was the Governor of Madhya Pradesh from 23 January 2018 till 28 July 2019.</p>
