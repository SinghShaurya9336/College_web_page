	<h1 align="center">RLA University</h1>
	<p>The government of Uttar Pradesh, established Avadh University, Ayodhya, initially as an affiliating university by it's notification No. 1192/fifteen-10-46(6)-1975 dated 04 March, 1975 and appointed Prof.(Dr.) Surendra Singh as the first Vice chancellor. In 1993-94, it was renamed as Dr. Rammanohar Lohia Avadh University, Ayodhya in the memoriam of late Dr. Rammanohar Lohia, an epic socio-economic ideologue and freedom fighter par excellence.</p>
   <br/>
 <p>The university initially started its office in a rental building at Civil Lines, Ayodhya. Land acquisition process for varsity's formal office started in 1976. Ultimately, the then Chancellor and the Governer of state Shri G.D. Tapase laid foundation of the present administrative building of the varsity on 02 May 1978.</p>
    <br/>
 <p>The university assumed the shape of an affiliating cum residential varsity in April 1984. The residential segment became functional with the opening of four PG departments in the campus viz. History, Culture & Archaeology, Economics & Rural Development.</p>
